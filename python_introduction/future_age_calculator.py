# Prompt the user to input their current age
current_age = int(input("How old are you? "))  # Takes input from the user and converts it to an integer

# Calculate the user's age in 2050
future_age = current_age + 27  # Adds 27 years to the current age (since 2050 - 2023 = 27)

# Print the result
print(f"In 2050, you will be {future_age} years old.")  # Displays the calculated future age









